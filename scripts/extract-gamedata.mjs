// Extracts species (names, base stats, types, abilities), ability names, and move data
// from the Pokemon Emerald Imperium ROM. Imperium is built on pokeemerald-expansion.
//
// Offsets located empirically against known species/moves (see scripts/README notes):
//   gSpeciesInfo : base 0xD42658, stride 260.
//       +0..5  base stats [HP,Atk,Def,Spe,SpA,SpD] (u8)
//       +6,+7  types[2] (u8; TYPE_NONE=0, Normal=1 … Fairy=19)
//       +24,+26,+28  abilities[3] (u16: slot0, slot1, hidden)
//       +44    name (12 bytes, Gen-3 charmap, 0xFF-terminated)
//   gAbilityNames: base 0x6F2ED0, stride 28 (name at start of each entry)
//   gMovesInfo  : base 0x6E3708, stride 56.
//       +0     const u8 *name  (GBA pointer 0x08……; deref for the name string)
//       +10    bits0-4 = type, bit5 (0x20) = special, bit6 (0x40) = status, else physical
//       +11    power (u8)
//       +12    accuracy (u8)
//       +14    pp (u8)
//
// The app ships the generated files so the ROM isn't needed at runtime.
// Run: node scripts/extract-gamedata.mjs ["C:/path/to/rom.gba"]
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const ROM_PATH = process.argv[2] || 'C:/Users/parke/Pokemon Docs/Pokemon Imperium v1.1.gba';
const __dirname = dirname(fileURLToPath(import.meta.url));
const DATA = resolve(__dirname, '../src/data');

const rom = new Uint8Array(readFileSync(ROM_PATH));
const u16 = (o) => rom[o] | (rom[o + 1] << 8);
const u32 = (o) => (rom[o] | (rom[o + 1] << 8) | (rom[o + 2] << 16) | (rom[o + 3] << 24)) >>> 0;
const GBA_BASE = 0x08000000;

// Gen-3 in-game charmap (uppercase, lowercase, digits + common punctuation).
const CH = { 0xff: null, 0x00: ' ', 0xae: '-', 0x2e: '.', 0xb4: "'", 0xba: '/', 0x2d: '&' };
for (let c = 65; c <= 90; c++) CH[0xbb + (c - 65)] = String.fromCharCode(c);
for (let c = 97; c <= 122; c++) CH[0xd5 + (c - 97)] = String.fromCharCode(c);
for (let c = 48; c <= 57; c++) CH[0xa1 + (c - 48)] = String.fromCharCode(c);
function decode(o, len) {
  let s = '';
  for (let k = 0; k < len; k++) {
    const ch = CH[rom[o + k]];
    if (ch === null || ch === undefined) break;
    s += ch;
  }
  return s.trim();
}
const looksName = (s) => s && /[A-Za-z]/.test(s) && !s.includes('?');

/* ---------------- Species ---------------- */
const SP_BASE = 0xd42658, SP_STRIDE = 260;
const species = {};
let blanks = 0;
for (let id = 1; id < 2200; id++) {
  const o = SP_BASE + id * SP_STRIDE;
  const name = decode(o + 44, 12);
  if (!looksName(name)) { if (++blanks > 60) break; continue; }
  blanks = 0;
  const t1 = rom[o + 6], t2 = rom[o + 7];
  const types = t1 === t2 ? [t1] : [t1, t2];
  const abilities = [u16(o + 24), u16(o + 26), u16(o + 28)];
  species[id] = {
    n: name,
    s: [rom[o], rom[o + 1], rom[o + 2], rom[o + 3], rom[o + 4], rom[o + 5]],
    t: types,
    a: abilities,
  };
}

/* ---------------- Ability names ---------------- */
const AB_BASE = 0x6f2ed0, AB_STRIDE = 28;
const abilities = {};
for (let id = 0; id < 400; id++) {
  const name = decode(AB_BASE + id * AB_STRIDE, 16);
  // id 0 is the "-------" placeholder; keep real names only.
  if (looksName(name) && !/^-+$/.test(name)) abilities[id] = name;
}

/* ---------------- Moves ---------------- */
const MV_BASE = 0x6e3708, MV_STRIDE = 56;
const moves = {};
blanks = 0;
for (let id = 1; id < 1000; id++) {
  const o = MV_BASE + id * MV_STRIDE;
  const ptr = u32(o);
  if (ptr < GBA_BASE || ptr >= GBA_BASE + rom.length) { if (++blanks > 40) break; continue; }
  const name = decode(ptr - GBA_BASE, 16);
  if (!looksName(name)) { if (++blanks > 40) break; continue; }
  blanks = 0;
  const flags = rom[o + 10];
  const cat = (flags & 0x40) ? 'status' : (flags & 0x20) ? 'spec' : 'phys';
  moves[id] = {
    n: name,
    t: flags & 0x1f,           // type id
    c: cat,                    // phys | spec | status
    p: rom[o + 11],            // power
    acc: rom[o + 12],          // accuracy (0 = never misses, e.g. Swift)
    pp: rom[o + 14],
  };
}

/* ---------------- Write ---------------- */
mkdirSync(DATA, { recursive: true });
const write = (file, varName, obj, note) => {
  const banner = `// AUTO-GENERATED by scripts/extract-gamedata.mjs — do not edit by hand.\n// ${note}\n`;
  writeFileSync(resolve(DATA, file), `${banner}export const ${varName} = ${JSON.stringify(obj)};\n`);
};
write('species.js', 'SPECIES', species,
  `${Object.keys(species).length} species. Shape: {n:name, s:[HP,Atk,Def,Spe,SpA,SpD], t:[type…], a:[ability ids]}.`);
write('abilities.js', 'ABILITIES', abilities, `${Object.keys(abilities).length} ability names by id.`);
write('moves.js', 'MOVES', moves,
  `${Object.keys(moves).length} moves. Shape: {n, t:typeId, c:phys|spec|status, p:power, acc, pp}.`);

console.log(`species:${Object.keys(species).length} abilities:${Object.keys(abilities).length} moves:${Object.keys(moves).length}`);
console.log('spot species:', [1, 25, 642].map((i) => `${species[i].n} t=${species[i].t} a=${species[i].a}`).join(' | '));
console.log('spot moves:', [1, 53, 85].map((i) => `${moves[i].n}(t${moves[i].t}/${moves[i].c}/p${moves[i].p})`).join(' '));
